public class LetterPair {
    private char upper;
    private char lower;

    public LetterPair(char upper, char lower) {
        this.upper = upper;
        this.lower = lower;
    }

    public char getUpper() {
        return upper;
    }

    public char getLower() {
        return lower;
    }
}
